// Rules for Naming Variables

// You cannnot start with Number
// example :
// 1value (invalid)
// value1 (valid)

// You can use only underscore(_) and dollar($) with variables name
// firstName (valid)
// $firstName (valid)
// _firstName (valid)

// You can not use Spaces
// first Name (invalid)

// Convention
// Start with Small Letters and Use camelCase